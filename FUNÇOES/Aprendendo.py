''' Função em python é uma rotina, ou seja uma rotina é aquilo que você faz constantemente.
Por exempo Para fazer um programa bonito com linhas em cima e embaixo você usa print('-=-'*30) e você usa
print um monte de vezes so para colocar linhas. Então para não ficar fazendo isso cria uma função.
Então por exemplo: 
def mostrarLinha():
    print('-=-'*30)
mostrarLinha()
print('Sistema de Alunos)
mostrarLinha()
mostrarLinha()
print('Cadastro de Funcionarios')
mostrarLinha()
mostrarLinha():
print('Erro no Sistema)
mostrarLinha()

O mostrarLinha() vai mostra o que está dentro da rotina(def)
'''

